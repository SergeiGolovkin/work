package home_work_1;

public class HomeWork3 {
    public static void main(String[] args) {
        System.out.println(Math.sin(Math.toRadians(90))); // метод вычисляет значение sin для угла 90 гравдусов.
        System.out.println(Math.pow(5,4));                // метод возведение в 4 степень числа 5.
        System.out.println(Math.cos(Math.toRadians(30))); // метод вычисляет значение cos для угла 90 гравдусов.
        System.out.println(Math.abs(-33));                // метод выводит значение по модулю
        System.out.println(Math.cbrt(27));                // метод извлекает корень
        System.out.println(Math.ceil(32.3));              // метод округляет число в большую сторону
        System.out.println(Math.floor(32.8));             // метод округляет число в меньшую сторону
        System.out.println(Math.round(1.4));              // метод округляет число по правилам математики
        System.out.println(Math.random());                // метод выводит случайное число от 0 до 1
        System.out.println(Math.max(5,8));                // метод выводит большее значение

    }
}
